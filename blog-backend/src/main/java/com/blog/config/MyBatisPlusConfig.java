package com.blog.config;

import com.baomidou.mybatisplus.annotation.DbType;
import com.baomidou.mybatisplus.extension.plugins.MybatisPlusInterceptor;
import com.baomidou.mybatisplus.extension.plugins.inner.PaginationInnerInterceptor;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

/**
 * MyBatis-Plus configuration. Registers the pagination plugin so that
 * {@code Page<T>} objects used in service/controller layers are automatically
 * translated into database-level LIMIT/OFFSET queries for MySQL.
 */
@Configuration
public class MyBatisPlusConfig {

    /** Register the MyBatis-Plus interceptor chain with MySQL pagination support. */
    @Bean
    public MybatisPlusInterceptor mybatisPlusInterceptor() {
        MybatisPlusInterceptor interceptor = new MybatisPlusInterceptor();
        interceptor.addInnerInterceptor(new PaginationInnerInterceptor(DbType.MYSQL));
        return interceptor;
    }
}
